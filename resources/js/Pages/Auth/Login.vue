                    </path>
                </svg>
                <input placeholder="Password" class="input-field" id="password"
                    v-model="form.password"
                    type="password"  required
                    autocomplete="current-password">
            </div>
                
            <div class="btn">
                <PrimaryButton class="button1" :class="{ 'opacity-25': form.processing }" :disabled="form.processing">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Login&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</PrimaryButton>
                <Link :href="route('register')" class="button2">Register</Link>
            </div>
            <Link v-if="canResetPassword" :href="route('password.request')" class="underline text-center text-sm text-white hover:text-gray-900 p-2">Forgot Password</Link>
